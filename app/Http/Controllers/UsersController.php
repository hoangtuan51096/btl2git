    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $money = User::find($id)->wallet;
        $data = User::find($id);
        if (empty($money)) {
            $data['money'] = 0;
            $data['vi'] = '';
        } else {
            $data['money'] = $money->money;
            $data['vi'] = $money->name;
        }
        return view('Show')->with('userbyID', $data);
    }

    /**
